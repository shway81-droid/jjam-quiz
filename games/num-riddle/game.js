function shuffle(arr) {
  const a = arr.slice();
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function randInt(min, max) {   // inclusive
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function clearTimers() {
  if (countdownInterval) { clearInterval(countdownInterval); countdownInterval = null; }
  if (timerHandle) { clearInterval(timerHandle); timerHandle = null; }
  if (nextHandle)  { clearTimeout(nextHandle);   nextHandle  = null; }
}

// ── 문제 생성 ────────────────────────────────────────────────
// 비밀 수 S에 대해 참인 힌트 2~3개를 만든다.
// 오답(distractor)은 힌트 중 하나 이상을 반드시 어기게 골라, 보기 4개 중 정답은 유일.
const MARKS = ['①', '②', '③'];

function buildClues(S) {
  const cands = [];

  // 크기 힌트
  const lo = S - randInt(1, Math.min(9, S - 1));
  if (lo >= 1) cands.push({ text: `${lo}보다 커요`, pred: n => n > lo });
  const hi = S + randInt(1, 9);
  if (hi <= 100) cands.push({ text: `${hi}보다 작아요`, pred: n => n < hi });

  // 홀짝
  if (S % 2 === 0) cands.push({ text: '짝수예요', pred: n => n % 2 === 0 });
  else             cands.push({ text: '홀수예요', pred: n => n % 2 === 1 });

  // 자릿수
  if (S >= 10) cands.push({ text: '두 자리 수예요', pred: n => n >= 10 });
  else         cands.push({ text: '한 자리 수예요', pred: n => n < 10 });

  // 일의 자리
  cands.push({ text: `일의 자리가 ${S % 10}이에요`, pred: n => n % 10 === (S % 10) });

  // 십의 자리 (두 자리 수만)
  if (S >= 10) {
    const t = Math.floor(S / 10);
    cands.push({ text: `십의 자리가 ${t}예요`, pred: n => Math.floor(n / 10) === t });
  }

  // 배수 힌트
  const mults = [3, 5].filter(k => S % k === 0);
  if (mults.length) {
    const k = mults[randInt(0, mults.length - 1)];
    cands.push({ text: `${k}의 배수예요`, pred: n => n % k === 0 });
  }

  const picked = shuffle(cands);
  const count  = picked.length >= 3 && Math.random() < 0.6 ? 3 : 2;
  if (picked.length < 2) return null;
  return picked.slice(0, Math.min(count, picked.length));
}

function buildProblem() {
  let guard = 0;
  while (guard++ < 300) {
    const S = randInt(4, 99);
    const clues = buildClues(S);
    if (!clues) continue;

    const preds = clues.map(c => c.pred);
    const satisfiesAll = n => preds.every(p => p(n));

    const distract = [];
    let g2 = 0;
    while (distract.length < 3 && g2++ < 500) {
      const n = randInt(1, 99);
      if (n === S || distract.includes(n)) continue;
      if (satisfiesAll(n)) continue;   // 힌트 중 하나 이상을 어겨야 오답
      distract.push(n);
    }
    if (distract.length < 3) continue;

    const display = clues.map((c, i) => `${MARKS[i]} ${c.text}`).join('<br>');
    const choices = shuffle([S, ...distract].map(String));
    return { display: display, answerStr: String(S), choices: choices };
  }
  // 안전 폴백
  return { display: '① 짝수예요<br>② 5보다 커요', answerStr: '8', choices: ['8', '3', '7', '9'] };
}

function buildRounds(n) {
  const rounds = [];
  const seen = new Set();
  let guard = 0;
  while (rounds.length < n && guard++ < 500) {
    const p = buildProblem();
    if (seen.has(p.display)) continue;
    seen.add(p.display);
    rounds.push(p);
  }
  return rounds;
}

