let scores         = [];
let roundLog       = [];
let currentRound   = null;   // { opponentKey, promptId, correctKey }
let currentChoices = HAND_KEYS.slice();
let dqSet          = new Set();
let phase          = 'idle';
let timerHandle    = null;
let nextHandle     = null;
let timeRemaining  = ROUND_TIME;
let gameRounds     = [];

