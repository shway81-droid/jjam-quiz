function rand(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function shuffle(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = rand(0, i);
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

// ── Question generation ──────────────────────────────────────
function generateQuestion() {
  const item = SOUND_DATA[rand(0, SOUND_DATA.length - 1)];

  // 3 distinct wrong sounds from other entries (sounds are globally unique).
  const others = SOUND_DATA.filter(d => d.sound !== item.sound);
  shuffle(others);
  const wrong = others.slice(0, 3).map(d => d.sound);

  const choices = shuffle([item.sound, ...wrong]);

  return { emoji: item.emoji, name: item.name, answer: item.sound, choices };
}

